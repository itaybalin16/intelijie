public class Calculator {
    public static int sumNums(int num1, int num2){
        return num1 + num2;
    }
    public static int subtructNums(int num1, int num2){
        return num1 - num2;
    }
    public static int mupltiplieNums(int num1, int num2){
        return num1 * num2;
    }
    public static int divideNums(int num1, int num2){
        return num1 / num2;
    }
}
